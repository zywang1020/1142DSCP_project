def parse_class_time(text):
    text = str(text)
    pattern = r"([一二三四五六日])\s+([0-9A-D]+)-([0-9A-D]+)"
    match = re.search(pattern, text)

    if match:
        day = match.group(1)
        start_period = match.group(2)
        end_period = match.group(3)

        return pd.Series([
            day_map.get(day),
            start_period,
            end_period,
            period_order.get(start_period),
            period_order.get(end_period)
        ])

    return pd.Series([None, None, None, None, None])

