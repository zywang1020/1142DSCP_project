"""### 整合課表與複習計畫 (Timetable Style)
此區塊將自動計算空檔時段，並填入複習任務。
"""

import pandas as pd
import numpy as np
from datetime import datetime, timedelta

# 1. 定義時間段 (08:00 - 22:00)
time_slots = [f"{h:02d}:00" for h in range(8, 22)]
days_of_week = ['Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday', 'Sunday']

# 2. 獲取本週日期範圍 (以今天開始的 7 天為例)
today = datetime.now().date()
date_range = [today + timedelta(days=i) for i in range(7)]

# 初始化課表數據矩陣
schedule_df = pd.DataFrame(index=time_slots, columns=[d.strftime('%Y-%m-%d (%a)') for d in date_range])
schedule_df.fillna("---(空閒)---", inplace=True)

# 3. 標記正課時段 (避開時段)
for _, row in my_courses.iterrows():
    day_name = row['day']
    start_h = row['start_order'] + 7 # 假設節次 1 是 08:00
    end_h = row['end_order'] + 7

    for dt in date_range:
        if dt.strftime('%A') == day_name:
            col = dt.strftime('%Y-%m-%d (%a)')
            for h in range(start_h, end_h + 1):
                slot = f"{h:02d}:00"
                if slot in schedule_df.index:
                    schedule_df.at[slot, col] = f"🚫 正課: {row['中文課程名稱']}"

# 4. 安排複習進度 (根據距離與重要度)
if not exams.empty:
    # 計算權重：(重要度 * 5) / (距離天數 + 1)
    exam_task_pool = []
    for _, ex in exams.iterrows():
        days_left = (ex['exam_date'].date() - today).days
        if days_left >= 0:
            # 依照權重決定複習總時數 (簡單公式：重要度 * 2)
            hours_needed = int(ex['importance'] * 2)
            exam_task_pool.extend([ex['subject']] * hours_needed)

    # 將複習任務填入空閒時段
    task_idx = 0
    for col in schedule_df.columns:
        for slot in schedule_df.index:
            if schedule_df.at[slot, col] == "---(空閒)---" and task_idx < len(exam_task_pool):
                schedule_df.at[slot, col] = f"📖 複習: {exam_task_pool[task_idx]}"
                task_idx += 1

# 5. 呈現圖表資訊
print("📅 未來 7 天整合課表與複習計畫：")
display(schedule_df.style.set_properties(**{
    'text-align': 'center',
    'border': '1px solid black',
    'background-color': '#f9f9f9'
}))

